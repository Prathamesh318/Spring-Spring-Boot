package com.infosys.firstproject.beans;

public class Employee {
	int empId;
	String employeeName;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId=empId;
	}
	
	
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName=employeeName;
	}
}
